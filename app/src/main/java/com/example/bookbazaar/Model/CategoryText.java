package com.example.bookbazaar.Model;

public class CategoryText {

    // Property to store the category
    String Category = "";

    // Constructor to initialize the category
    public CategoryText(String category) {
        Category = category;
    }

    // Getter to retrieve the category
    public String getCategory() {
        return Category;
    }

    // Setter to set the category
    public void setCategory(String category) {
        Category = category;
    }
}
